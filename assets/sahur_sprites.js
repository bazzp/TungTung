const SAHUR_COLORS = [
    'rgba(0,0,0,0)',  // 0 - przezroczyste
    '#dcb67b',        // 1 - pała (jasny brąz)
    '#f0d7b1',        // 2 - nogi (beż)
    '#e3c180',        // 3 - stopy (jasny beż)
    '#f6e1c0',        // 4 - ramiona/skóra
    '#fff',           // 5 - detal
    '#ccc',           // 6 - detal
    '#111',           // 7 - kontur
    '#a26522',        // 8 - drewno (korpus)
];

// Funkcja do rysowania wybranej klatki sprite'a Sahura
function drawSahurSprite(ctx, x, y, frame, flip=false) {
    const sprite = SAHUR_SPRITES[frame];
    for (let row = 0; row < sprite.length; row++) {
        for (let col = 0; col < sprite[row].length; col++) {
            const colorIdx = sprite[row][col];
            if (colorIdx !== 0) {
                ctx.fillStyle = SAHUR_COLORS[colorIdx];
                const drawX = flip ? x + sprite[row].length - 1 - col : x + col;
                ctx.fillRect(drawX, y + row, 1, 1);
            }
        }
    }
}